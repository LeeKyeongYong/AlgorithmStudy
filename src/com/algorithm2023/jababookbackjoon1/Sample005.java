package com.algorithm2023.jababookbackjoon1;

import java.util.Scanner;

public class Sample005 {//백준 2742 기찍 N
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int N=sc.nextInt();
		sc.close();
		for(int i=N;i>0;i--) {
			System.out.println(i);
		}
	}
}
